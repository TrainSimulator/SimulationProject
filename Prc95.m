function prc95 = Prc95(data)

% ============================================================================
% DESCRIPTION
%
% usage: prc95 = Prc95(data)
%
% Calculate the 95 percentile of data
%
% ----------------------------------------------------------------------------
% PARAMETERS
%
% data    (vector) data vector
%
% ---------------------------------------------------------------------------
% RETURN VALUES
%
% prc95   95 percentile of data
% 
% ============================================================================

prc95 = prctile(data,95);
